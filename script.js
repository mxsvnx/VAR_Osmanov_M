        // Функция для создания новостных карточек
        function createNewsCards() {
            const newsContainer = document.getElementById('news-container');
            
            // Ограничиваем до 6 новостей для главной страницы
            const newsToShow = newsData.slice(0, 6);
            
            newsToShow.forEach((news, index) => {
                const newsCard = document.createElement('article');
                newsCard.className = 'news-card';
                newsCard.innerHTML = `
                    <div class="card-content">
                        <span class="news-category">${news.category}</span>
                        <div class="news-date">${formatDate(news.date)}</div>
                        <h2 class="news-title">${news.title}</h2>
                        <div class="news-preview" id="preview-${index}">
                            <p>${news.preview}</p>
                        </div>
                        <button class="read-more" data-index="${index}">Читать далее</button>
                    </div>
                `;
                newsContainer.appendChild(newsCard);
            });
        }

        // Функция для форматирования даты
        function formatDate(dateString) {
            const options = { year: 'numeric', month: 'long', day: 'numeric' };
            return new Date(dateString).toLocaleDateString('ru-RU', options);
        }

        // Функция для обработки кликов по кнопкам "Читать далее"
        function setupToggleButtons() {
            document.addEventListener('click', function(e) {
                if (e.target.classList.contains('read-more')) {
                    const index = e.target.getAttribute('data-index');
                    const preview = document.getElementById(`preview-${index}`);
                    
                    if (preview.classList.contains('expanded')) {
                        preview.classList.remove('expanded');
                        e.target.textContent = 'Читать далее';
                    } else {
                        preview.classList.add('expanded');
                        e.target.textContent = 'Свернуть';
                    }
                }
            });
        }

        // Инициализация при загрузке страницы
        document.addEventListener('DOMContentLoaded', function() {
            createNewsCards();
            setupToggleButtons();
        });